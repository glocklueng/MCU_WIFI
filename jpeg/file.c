extern int remote_socket_fd;
int _fwrite(void * buf, size_t size_of_type, size_t count, int file)
{
	int ret;
	uint8_t *p_buf;
	size_t buf_size, remain;
#if 1
	p_buf = (uint8_t*)buf;
	buf_size = count*size_of_type;
#else	//for test
	p_buf = (uint8_t*)test_jpeg;
	buf_size = sizeof(test_jpeg);
#endif		
	remain = buf_size;	
	if(remote_socket_fd != -1)
	{
again:
		ret = send(remote_socket_fd, p_buf, remain, 0);
		
		if(ret != remain){

			if(ret > 0)
			{
				remain -= ret;
				if(remain > 0)
				{
					p_buf += ret;
					goto again;
				}
			}
			p_err("send data err:%d", ret);
			close_socket(remote_socket_fd);
			remote_socket_fd = -1;
		}
	}
	return buf_size;
}

